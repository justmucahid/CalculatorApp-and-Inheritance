package polymorphism;

public class Me {

    private String name;
    private int age;
    private String urlPng;
    private double hight;
    private double weight;
    private double footSize;
    private boolean isIslam;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getUrlPng() {
        return urlPng;
    }

    public void setUrlPng(String urlPng) {
        this.urlPng = urlPng;
    }

    public double getHight() {
        return hight;
    }

    public void setHight(double hight) {
        this.hight = hight;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public double getFootSize() {
        return footSize;
    }

    public void setFootSize(double footSize) {
        this.footSize = footSize;
    }

    public boolean isIslam() {
        return isIslam;
    }

    public void setIslam(boolean isIslam) {
        this.isIslam = isIslam;
    }

}
